/**
 * Builds `lib/tenant/tenant-init.sql` from the latest generated Drizzle tenant
 * migration, prepending the pgvector extension (required by the 3072-dim
 * embedding columns). This file is applied verbatim to every new Neon branch
 * during user provisioning.
 *
 * Run: npm run db:build-tenant-init  (after db:generate:tenant)
 */
import { readdirSync, readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { join } from "node:path";

const root = process.cwd();
const tenantDir = join(root, "drizzle", "tenant");
const outFile = join(root, "lib", "tenant", "tenant-init.sql");

const migrations = readdirSync(tenantDir)
  .filter((f) => f.endsWith(".sql"))
  .sort();

if (migrations.length === 0) {
  throw new Error("No tenant migrations found. Run `npm run db:generate:tenant` first.");
}

// Concatenate every migration in order so a fresh branch gets the full schema.
const body = migrations
  .map((f) => readFileSync(join(tenantDir, f), "utf8").trim())
  .join("\n--> statement-breakpoint\n");

const header = [
  "-- AUTO-GENERATED by scripts/build-tenant-init.mjs — do not edit by hand.",
  "-- Applied to every new per-user Neon branch during provisioning.",
  "CREATE EXTENSION IF NOT EXISTS vector;",
].join("\n");

mkdirSync(join(root, "lib", "tenant"), { recursive: true });
writeFileSync(outFile, `${header}\n--> statement-breakpoint\n${body}\n`);
console.log(`Wrote ${outFile} from ${migrations.length} migration(s).`);
